package practice_day03_12_15_2021;

public class SwapTwoNum {
    public static void main(String[] args) {


        int a = 10;
        int b = 15;

        int temp = a;
        a = b; //a=15
        b = temp; //b=10

        System.out.println("a = " + a);
        System.out.println("b = " + b);

    }
}
